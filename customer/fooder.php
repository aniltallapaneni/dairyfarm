<b>Stylo:</b><br> The stylo plant is native to Brazil and it can grow up to a height of 0.6 to 1.8 meters. It is very suitable for cultivating in the tropical regions and also in the regions or lands with low fertility, drought, acidic soils and poor drainage conditions. Crude protein in the stylo leaves is between 15 and 18 percent. You can expect to have the first harvest around 75 days after sowing the seeds.

<b>Tree Fodder Varieties</b><br>
The tree fodder varieties are those varieties which are considered as best fodder crops for dairy cattle. Here we are shortly describing about some popular tree fodder crop varieties.<br>

<b>1.Glyricidia:</b> Clyricidia crop can be grown and tolerate a wide variety of climatic conditions. And it can be grown in a wide variety of soil types including clay, sandy and rocky soil with good drainage system. The glyricidia plants trees are generally grown as ornamental trees or as shade trees in coffee gardens. The green leaf manure produced from this tree variety is very popular, because it helps in fixing nitrogen in the soil.<br>
<b>2.Sesbania:</b>The sesbania trees can be grown in all types of soil and it can be grown throughout the year. Average protein content in the sesbania tree leaves is about 25 percent. You can expect to have the first harvest generally after 8 months of sowing the seeds with an annual production of around 100 tonnes.<br>

<b>3.Subabul:</b>The subabul crop is grown under rain-fed conditions. It has many seasonal varieties. You can expect to have the first harvest when the tree trunk reaches a diameter of 3 cm and has completed one seed production cycle. Subsequent harvest are generally done 40-80 days after the first harvest. Average annual yield of the subabul trees is between 80 and 100 tonnes.<br>

These are the best fodder crops for dairy cattle. If you have land, then you should consider growing any of these fodder crop in your land for feeding your dairy cattle. Nothing can take place of the green fodder for dairy cattle. Because green fodder is very important for increasing milk production.<br>

<b>Monitor:</b><br>
Your cow can get sick sometimes. So, you must have to take good care of the cow for keeping it healthy. Proper handling, quality feeding and timely vaccinations can help you to keep your cow free from most of the health related problems.<br>

<b>Hand Milk:</b><br>
Before start milking with the machine, you need to hand milk each teat for a few times to let down the milk. And as we have mentioned previously, this system is called ‘stripping’. Stripping will not only encourage milk to flow from the teat, but also help to squirt out any dirt or bacteria that has built up in the cow’s teat. You should not take the milk produced from stripping, because it may contaminate the rest of the milk.<br>

<b>Clean the Cow’s Udder & Teats:</b><br>
          Perfectly clean the udder and teats of the cow after successful milking by machine. Because cow’s teats are vulnerable to bacteria and infection immediately after having been milked. So their should be properly cleaned and protected from bacteria. You can apply a post-milking disinfectant for protecting your cow.</p>

