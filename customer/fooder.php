<p>Best fodder crops for dairy cattle are those crops which are considered best for the dairy cattle. Growing greens for your animals in your own land is very important. Because it saves a lot of money and at the same time you can feed your animals with very good quality foods.
Growing the greens of your own is very important for running a successful business, and it also considered economical. Most of the cattle farmers don’t know about the importance of growing green fodder of their own. They rather focus mainly on feeding their cattle with concentrated feed. And relying only on concentrated feeds incurs great investment.
But feeding the dairy cattle with green fodder which are grown in your own land has many advantages. Feeding the dairy cattle with green fodder will help to reduce the expenses for concentrate feeds and which will ultimately increase the total amount of profits from the farm.
So, there are lots of advantages of growing green fodder of your own. Here in this guide we are trying to discuss more about the steps for growing the best fodder crops for dairy cattle.<br>

Cereal fodder category has many different varieties. Read below for more details.<br>
<b>Co-4 Grass:</b><br>
     Co-4 grass is produced from the cross of Napier grass and Bajra cereal. It is considered as a high yielding variety, producing up to or around 150 tonnes per acre of land per year. The average protein content in Co-4 grass is between 8 and 11 percent. It has a planting rate of around 17,000 slips per acre.<br>

<b>Maize Fodder:</b><br>
   Maize fodder can be grown on a wide variety of soil types under well drained and fertile conditions. The maize is mostly grown as a kharif crop during the month of June-July. There are many varieties of maize available which are grown as cattle fodder. Among these varieties African tall, Jawahar, Ganga-5, Moti composite and Vijay composite are very popular in India.<br>

<b>Sorghum Fodder:</b><br>
Sorghum is also considered as one of the best fodder crops for dairy cattle. The sorghum fodder is cultivated both for grains and green fodder, and it is highly drought tolerant. You can plant sorghum throughout the year, and many varieties are available. But it grows best under temperature ranging between 25 0C and 35 0C. For growing sorghum, you will need about 40 kg seeds per hectare, for using the sorghum as fodder, the grass can be harvested after the flowering stage. Any type of soil is suitable for sorghum cultivation.<br>

<b>Grass Fodder Varieties</b><br>
In the grass fodder category, there are many other sub-varieties available. Here we are describing more about the popular grass fodder varieties.<br>

<b>Blue Buffalo Grass:</b><br> The Blue Buffalo grass grows well in dry lands under rain-fed conditions. 2 major varieties of this grass are C. Ciliaris is and C. Setigerus. The soil which is rich in calcium content and well drained, are considered very good for growing Blue Buffalo grass. Generally 6-8 kg seeds are required for growing in 1 hectare land. You can harvest this grass for the first time after 70-75 days of planting the seeds. And you will be able to harvest 4-6 cuts each year with an average yield of 40 tonnes per hectare.<br>

<b>Guinea Grass: </b><br>The Guinea grass is a short creeping rhizome which is propagated very easily either by seeds or by slips (around 2.5 kg seeds required per hectare and around 66,000 slips). It can grows in any type of soil with very good drainage system. Guinea grass is rich in crude protein content, containing around 4 to 14 percent. You can expect the first harvest after 75-80 days from germinating the seeds, and around 45 days after planting the slips. Average yield per hectare is around 175 tonnes per year in 8 cuts.<br>

<b>Hybrid Napier:</b><br> The Hybrid Napier grass has more number of leaves and are considered as best fodder crops for dairy cattle. The plants are vigorous and can be grown in a wide variety of soil types. The crude protein content in the hybrid Napier grass is between 8 and 11 percent. Propagation is done through planting the slips, and you will need about 40,000 slips per hectare. You can expect to have the first harvest around 45 days after planting the slips.<br>
Para Grass: The Para grass are best grown in the flooded areas with humid condition. But it is also suitable for growing in the arid and semi-arid areas, but the crop is sensitive to winter weather conditions. Seed setting is very poor in Para grass and the crop is mainly propagated through stem cuttings. Generally the stems with 2-3 nodes are used as planting material. You will need to have around 800-1000 kg of stems per hectare of land. You can expect the first harvest 75-80 days after planting the stems and average yield is between 80 and 100 tonnes per hectare with an average of 6-9 cuts per year.<br>


<b>Legume Fodder Varieties</b><br>
Some legumes are grown as fodder crop for feeding the dairy cattle. Some most popular legume fodder varieties are mentioned below.<br>

<b>Cow Pea: </b><br>The cow pea plant is native to tropical, sub-tropical and warm temperate regions. But the crop can be cultivated in any season throughout the year. Average crude protein content in cow pea is around 20 percent. You will need about 40 kg seeds for planting cow pea in 1 hectare land. You can expect to have first harvest 50-55 days after sowing the seeds. The cow pea legume variety is generally used for open grazing, and it is also considered as one of the best fodder crops for dairy cattle.<br>

<b>Desmanthus: </b><br>The desmanthus plants can be grown throughout the year as an irrigated crop. But in the Indian sub-continent it is grown during June-October as rain-fed crop. You will need about 20 kg seeds for planting desmanthus in one hectare land. You can expect to have first harvest 90 days after sowing the seeds.<br>

<b>Lucerne: </b><br>The lucerne crop is also called as alfalfa and it is considered to be a rich leguminous crio. It is called as the ‘Queen of Forages’, and considered as the best fodder crops for dairy cattle. It has a higher crude protein content of around 15-20 percent. lucerne crop helps to add nitrogen into the soil, and it helps to improve the fertility of the soil. It is mostly cultivated for green fodder, hay and silage. You will need about 20 kg seeds for cultivating lucerne in 1 hectare land. The lucerne is not suitable for open grazing, but it is highly digestible and helps in better milk production.<br>

